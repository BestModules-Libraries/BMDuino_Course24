/*
重要提醒：

1. 語音檔對應確認：
   - 使用前必須確認實際寫入語音模組的語音編號與本檔案定義一致
   - 若使用 BMduino Voice Widget 工具，請檢查語音清單中的編號
   - 語音編號從 0x00 開始，最多支援 256 段語音

2. 語音內容規劃建議：
   - 數字語音（0~9）建議錄製清晰、語調一致的版本
   - 單位語音（攝氏度、百分比）應使用標準用語
   - 警示語音應使用較為緊湊、醒目的語氣
   - 歡迎語可根據應用場景設計（如「歡迎使用智慧家庭系統」）

3. 編號擴充方式：
   - 若需要更多語音，可繼續定義 VOC_39、VOC_40...
   - 編號範圍為 0x00 ~ 0xFF，最多 256 段
   - 建議預留部分編號供未來擴充使用

4. 語句組合設計：
   - 可將多段語音組合成完整句子（如「現在溫度」+「25.5」+「攝氏度」）
   - 在 VocLib.h 中已提供 SayString(uint8_t, uint8_t) 支援兩段連續播放
   - 需要更多段組合時可自行擴充

5. 與主程式整合：
   - 本檔案需與 VocLib.h 一起使用
   - 在 VocLib.h 中已定義 voice_table 陣列對應這些 VOC_xx 常數
   - 使用時只需 #include "voice_cmd_list.h" 即可

6. 跨平台相容性：
   - 本檔案使用標準 C 語法，可在所有 Arduino 相容平台使用
   - 不依賴特定硬體，僅定義常數值
   - 可移植到其他支援語音模組的開發板

7. 程式碼可讀性：
   - 使用有意義的常數名稱（如 VOC_33）而非直接使用數值（0x20）
   - 可進一步定義更具語意的名稱，例如：
     #define VOICE_WARNING     VOC_33
     #define VOICE_CHECK       VOC_34
     #define VOICE_FRONT_DOOR  VOC_35

8. 版本管理：
   - 若語音內容更新，請同步更新本檔案中的註解說明
   - 建議記錄語音內容的修改歷史，方便日後維護
   - 可建立語音內容對照表文件，與程式碼分開管理

9. 記憶體考量：
   - 本檔案僅定義常數，不佔用 RAM
   - 所有定義在編譯時即被替換為數值
   - 不會影響程式執行時的記憶體使用量

10. 除錯建議：
    - 在開發階段，可在序列埠輸出即將播放的語音編號
    - 例如：Serial.print("Playing VOC_"); Serial.println(voc_number);
    - 方便確認語音播放是否正確對應
*/