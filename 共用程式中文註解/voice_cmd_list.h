/*
 * ==================================================
 * 檔案名稱：voice_cmd_list.h - 語音指令列表定義檔
 * 適用硬體：BMV31T001 語音播放模組 / BMduino 語音擴充板
 * 
 * 程式功能說明：
 *   本標頭檔定義了語音播放模組中所有語音段落的識別碼（ID）。
 *   這些識別碼對應到實際寫入語音模組中的語音檔案索引，
 *   開發者只需使用這些預定義的常數（如 VOC_1、VOC_2...），
 *   即可輕鬆呼叫語音模組播放對應的語音內容。
 * 
 * 語音系統架構說明：
 *   ┌─────────────────────────────────────────────────────────────────┐
 *   │                    語音模組系統架構                              │
 *   ├─────────────────────────────────────────────────────────────────┤
 *   │                                                                 │
 *   │   [語音檔案製作]          [語音工具寫入]         [程式碼呼叫]    │
 *   │   ┌─────────────┐        ┌─────────────┐        ┌───────────┐  │
 *   │   │ 錄製語音檔   │ ────→ │ BMduino     │ ────→ │ VOC_33    │  │
 *   │   │ (MP3/WAV)   │        │ Voice Widget│        │           │  │
 *   │   └─────────────┘        └─────────────┘        └───────────┘  │
 *   │          │                      │                      │         │
 *   │          ▼                      ▼                      ▼         │
 *   │   ┌─────────────┐        ┌─────────────┐        ┌───────────┐  │
 *   │   │ 語音檔案     │        │ 語音編號     │        │ 0x20      │  │
 *   │   │ "警告異常"   │ ←───→ │ 0x20        │ ←───→ │ (VOC_33)  │  │
 *   │   └─────────────┘        └─────────────┘        └───────────┘  │
 *   │                                                                 │
 *   │   語音內容對應關係：                                              │
 *   │   - VOC_1 ~ VOC_10：數字 0~9 或其他基礎語音                       │
 *   │   - VOC_11：小數點（「點」）                                     │
 *   │   - VOC_12：負號（「負」）                                       │
 *   │   - VOC_13：歡迎語                                              │
 *   │   - VOC_14：溫度提示（「現在溫度」）                              │
 *   │   - VOC_15：濕度提示（「現在濕度」）                              │
 *   │   - VOC_16：溫度單位（「攝氏度」）                                │
 *   │   - VOC_17：濕度單位（「百分比」）                                │
 *   │   - VOC_33 ~ VOC_38：入侵警示語音（不同區域）                     │
 *   │                                                                 │
 *   └─────────────────────────────────────────────────────────────────┘
 * 
 * 使用方式：
 *   1. 先使用 BMduino Voice Widget 工具將語音檔寫入 BMV31T001 模組
 *   2. 確保語音編號與本檔案中的定義一致
 *   3. 在程式中引入本標頭檔： #include "voice_cmd_list.h"
 *   4. 呼叫語音播放函式： SayString(VOC_33);  // 播放「警告：偵測到異常」
 * 
 * 語音編號規劃原則：
 *   - 0x00 ~ 0x09：數字 0~9 或其他基礎語音
 *   - 0x0A ~ 0x0F：特殊符號（點、負）及常用提示語
 *   - 0x10 ~ 0x1F：感測器相關語音（溫度、濕度、單位）
 *   - 0x20 ~ 0x25：警示語音（入侵偵測、異常警告）
 *   - 可根據實際需求擴充至 0xFF
 * 
 * 應用場景：
 *   - 智慧家電語音提示（開機、操作完成、錯誤警告）
 *   - 環境監測語音播報（溫度、濕度、空氣品質）
 *   - 門禁系統語音警示（「門已開啟」、「入侵警報」）
 *   - 互動式語音導覽系統
 *   - 工業設備狀態語音提醒
 * 
 * 作者/版本：Best Module / 語音指令列表
 * 建立日期：2025.03.27
 * 修改紀錄：
 *   - 2025.03.27：加入詳細繁體中文註解與整體說明
 * ==================================================
 */

/*! 
 * voice command list  
 * 語音指令列表檔案（通常由語音模組韌體或語音編碼器所對應）
 * 本檔案主要定義語音播放模組中，每一段語音對應的識別碼 (ID)。
 * 
 * 在實際使用時，開發者只需要呼叫：
 *     SayString(VOC_33);
 * 語音模組即可播放聲音編號 0x20 的音檔。
 *
 * 語音檔通常由語音工具（例如 BMduino Voice Widget）寫入語音模組中，
 * 每段語音都會有一個各自的索引值，這裡就是將這些索引值定義成常數。
 */

#ifndef _VOICE_CMD_LIST_H  // 防止重複引用（Include Guard）
#define _VOICE_CMD_LIST_H

// ================================================================
// =============== 語音代碼定義區 ===============
// ================================================================

/*  
 * ------------------ 語音代碼定義區 ------------------
 * 每一個 VOC_xx 對應語音模組中的一段語音。
 * 括號內的 hex（例如 0x00）是實際傳送給語音 IC 的語音 ID。
 *
 * 這些語音可藉由 BMduino 語音工具 (Voice Widget) 自行修改或替換，
 * 並下載至語音播放擴充板（如 BMV31T001）。
 *
 * 開發者在程式中只需要呼叫：
 *     SayString(VOC_35);
 * 語音模組便會播放與 0x22 對應的語音。
 */

// ---------- 基礎數字語音區 (0x00 ~ 0x09) ----------
// 通常對應數字 0~9 的語音，用於念讀數值
#define VOC_1          (0x00)   // 語音編號 1 → 通常為數字「0」或基礎語音
#define VOC_2          (0x01)   // 語音編號 2 → 通常為數字「1」
#define VOC_3          (0x02)   // 語音編號 3 → 通常為數字「2」
#define VOC_4          (0x03)   // 語音編號 4 → 通常為數字「3」
#define VOC_5          (0x04)   // 語音編號 5 → 通常為數字「4」
#define VOC_6          (0x05)   // 語音編號 6 → 通常為數字「5」
#define VOC_7          (0x06)   // 語音編號 7 → 通常為數字「6」
#define VOC_8          (0x07)   // 語音編號 8 → 通常為數字「7」
#define VOC_9          (0x08)   // 語音編號 9 → 通常為數字「8」
#define VOC_10         (0x09)   // 語音編號 10 → 通常為數字「9」

// ---------- 特殊符號語音區 (0x0A ~ 0x0F) ----------
// 用於處理數字字串中的特殊符號
#define VOC_11         (0x0A)   // 語音編號 11 → 通常為「點」（小數點）
#define VOC_12         (0x0B)   // 語音編號 12 → 通常為「負」（負號）

// ---------- 常用提示語音區 (0x0C ~ 0x0F) ----------
#define VOC_13         (0x0C)   // 語音編號 13 → 歡迎語（如「歡迎使用」）
#define VOC_14         (0x0D)   // 語音編號 14 → 溫度提示（如「現在溫度」）
#define VOC_15         (0x0E)   // 語音編號 15 → 濕度提示（如「現在濕度」）
#define VOC_16         (0x0F)   // 語音編號 16 → 溫度單位（如「攝氏度」）
#define VOC_17         (0x10)   // 語音編號 17 → 濕度單位（如「百分比」）

// ---------- 預留語音區 (0x11 ~ 0x1F) ----------
// 可根據實際需求自定義內容
#define VOC_18         (0x11)   // 語音編號 18 → 預留
#define VOC_19         (0x12)   // 語音編號 19 → 預留
#define VOC_20         (0x13)   // 語音編號 20 → 預留
#define VOC_21         (0x14)   // 語音編號 21 → 預留
#define VOC_22         (0x15)   // 語音編號 22 → 預留
#define VOC_23         (0x16)   // 語音編號 23 → 預留
#define VOC_24         (0x17)   // 語音編號 24 → 預留
#define VOC_25         (0x18)   // 語音編號 25 → 預留
#define VOC_26         (0x19)   // 語音編號 26 → 預留
#define VOC_27         (0x1A)   // 語音編號 27 → 預留
#define VOC_28         (0x1B)   // 語音編號 28 → 預留
#define VOC_29         (0x1C)   // 語音編號 29 → 預留
#define VOC_30         (0x1D)   // 語音編號 30 → 預留
#define VOC_31         (0x1E)   // 語音編號 31 → 預留
#define VOC_32         (0x1F)   // 語音編號 32 → 預留

// ---------- 警示語音區 (0x20 ~ 0x25) ----------
// 常用於入侵偵測、異常警告等場景
#define VOC_33         (0x20)   // 語音編號 33 → 常用於「警告：偵測到異常」
#define VOC_34         (0x21)   // 語音編號 34 → 常用於「請盡速檢查」類的尾語
#define VOC_35         (0x22)   // 語音編號 35 → 前門入侵警示
#define VOC_36         (0x23)   // 語音編號 36 → 客廳入侵警示
#define VOC_37         (0x24)   // 語音編號 37 → 倉庫入侵警示
#define VOC_38         (0x25)   // 語音編號 38 → 臥室入侵警示

// ---------- 擴充語音區 (0x26 ~ 0xFF) ----------
// 可根據實際需求繼續擴充至 0xFF（最多 256 段語音）
// #define VOC_39         (0x26)   // 語音編號 39
// #define VOC_40         (0x27)   // 語音編號 40
// ... 依此類推

// ================================================================
// =============== 語句組合定義區（可選擴充） ===============
// ================================================================

/* sentence */
/*
 * 若將來要加入「多語句語音組合」，
 * 可在這裡新增如：
 * 
 *   #define SENTENCE_WARNING   { VOC_33, VOC_34 }
 *   #define SENTENCE_TEMP      { VOC_14, VOC_16 }
 *   #define SENTENCE_HUMID     { VOC_15, VOC_17 }
 * 
 * 用於一次播放兩段或更多段語音，簡化程式碼。
 * 使用時可搭配 VocLib.h 中的 SayString() 多載版本：
 * 
 *   // 播放「現在溫度攝氏度」
 *   SayString(VOC_14, VOC_16);
 * 
 *   // 播放「警告：偵測到異常，請盡速檢查」
 *   SayString(VOC_33, VOC_34);
 */

#endif  // _VOICE_CMD_LIST_H

