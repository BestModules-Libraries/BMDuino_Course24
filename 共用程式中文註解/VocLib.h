{
    setVoiceLed(1);    // 播放期間 LED 亮

    sayText(vv);       // 先播放第一段語音
    delay(300);        // 兩段語音之間留 0.3 秒間隔
    sayText(vv2);      // 播放第二段語音
    setVoiceLed(0);    // 播放完成 LED 滅
}

