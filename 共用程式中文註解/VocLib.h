// ---------------------------------------------------------------
// 函式名稱：initVoice()
// 功能：初始化語音模組
// 參數：無
// 傳回值：無
// 
// 流程說明：
//   1. 呼叫 begin() 初始化模組通訊介面（I2C 或 UART）
//   2. 啟用模組電源
//   3. 進入音訊更新準備模式（若需要透過 PC 更新語音檔）
//   4. 延遲 100ms 等待模組穩定
//   5. 設定預設音量
// 
// 注意事項：
//   - 此函式必須在 setup() 中呼叫
//   - 若語音檔已寫入，initAudioUpdate() 仍可正常運作
// ---------------------------------------------------------------
void initVoice()
{
    // 步驟1：初始化 BMV31T001 模組
    // 設定通訊介面（I2C / UART 依函式庫實作）
    myBMV31T001.begin();
    
    // 步驟2：啟用語音模組電源
    // BMV31T001_POWER_ENABLE 為開啟電源
    myBMV31T001.setPower(BMV31T001_POWER_ENABLE);
    
    // 步驟3：進入音訊更新準備模式
    // 若要透過 PC 更新語音檔，此步驟必要
    // 若無需更新，此函式仍可呼叫，不影響正常播放
    myBMV31T001.initAudioUpdate();
    
    // 步驟4：延遲 100ms，讓模組穩定
    delay(100);
    
    // 步驟5：設定預設音量等級
    // DEFAULT_VOLUME = 9（接近最大音量）
    myBMV31T001.setVolume(DEFAULT_VOLUME);
}

// ---------------------------------------------------------------
// 函式名稱：getBoardKey()
// 功能：讀取語音模組板載按鍵狀態
// 參數：無
// 傳回值：uint8_t - 按鍵代碼（0=無按鍵，1=中鍵，2=上，4=下，8=左，16=右）
// 
// 使用說明：
//   1. 必須先呼叫 scanKey() 讓模組更新按鍵狀態
//   2. 透過 isKeyAction() 判斷是否有按鍵事件
//   3. 若有按鍵，則讀取按鍵代碼並回傳
// ---------------------------------------------------------------
uint8_t getBoardKey()
{
    // 步驟1：掃描按鍵狀態
    // 此函式會更新模組內部的按鍵狀態暫存器
    myBMV31T001.scanKey();

    // 步驟2：判斷是否有按鍵動作
    // isKeyAction() 回傳 BMV31T001_NO_KEY 表示無按鍵
    if (myBMV31T001.isKeyAction() != BMV31T001_NO_KEY)
        // 有按鍵：讀取按鍵代碼並回傳
        return myBMV31T001.readKeyValue();
    else
        // 無按鍵：回傳 0
        return 0x00;
}

// ---------------------------------------------------------------
// 函式名稱：DisplayVoiceStatus()
// 功能：顯示播放狀態，並同步控制模組上的 LED 指示燈
// 參數：無
// 傳回值：無
// 
// 說明：
//   - 當模組正在播放語音時，LED 亮起
//   - 當模組閒置時，LED 熄滅
//   - 可定期呼叫此函式來更新 LED 狀態
// ---------------------------------------------------------------
void DisplayVoiceStatus()
{
    // 步驟1：檢查播放狀態
    // isPlaying() 回傳 BMV31T001_BUSY 表示正在播放
    if (myBMV31T001.isPlaying() == BMV31T001_BUSY)
    {
        // 正在播放：更新狀態變數為「忙碌中」
        playStatus = BMV31T001_BUSY;
        // LED 亮起，表示正在播放
        myBMV31T001.setLED(BMV31T001_LED_ON);
    }
    else
    {
        // 未播放：狀態為「非忙碌」（閒置或停止）
        playStatus = BMV31T001_NOBUSY;
        // LED 熄滅
        myBMV31T001.setLED(BMV31T001_LED_OFF);
    }
}

