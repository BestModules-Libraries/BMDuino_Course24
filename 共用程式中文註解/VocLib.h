// ---------------------------------------------------------------
// 函式名稱：saynumber()
// 功能：播放單一數字（0~9）所對應的語音
// 參數：num - 要播放的數字（0~9）
// 傳回值：無
// 
// 流程說明：
//   1. 檢查數字是否在 0~9 範圍內
//   2. 等待前一段語音播放完畢
//   3. 播放對應數字的語音
// 
// 注意事項：
//   若傳入數字超出範圍，會在序列埠顯示錯誤訊息
// ---------------------------------------------------------------
void saynumber(int num)
{
    // 步驟1：檢查輸入範圍
    if (num < 0 || num > 9)
    {
        Serial.println("Wrong Number：僅允許 0~9 數字");
        return;
    }

    // 步驟2：等待前一段語音播放完畢
    // 避免語音重疊
    if (isPlaying())
        while (isPlaying());

    // 步驟3：播放對應數字語音
    // voice_table[num] 內存放的是對應數字的 VOC_xx 常數
    // 例如：num=0 → voice_table[0] = VOC_1（數字0的語音）
    //       num=1 → voice_table[1] = VOC_2（數字1的語音）
    while (!isPlaying())
    {
        play(voice_table[num]);
    }
}

// ---------------------------------------------------------------
// 函式名稱：checknum()
// 功能：檢查字串是否為「單一數字字元」（0~9）
// 參數：ss - 要檢查的字串
// 傳回值：boolean - true：是單一數字，false：不是數字或長度不為1
// 
// 使用範例：
//   checknum("5") → true
//   checknum("12") → false（長度不為1）
//   checknum("A") → false（不是數字）
// ---------------------------------------------------------------
boolean checknum(String ss)
{
    String numberchar = "0123456789"; // 合法數字字元集合

    // 若長度不是 1，必定不是單一數字
    if (ss.length() != 1)
        return false;

    // 判斷字串中唯一的字元是否存在於 numberchar 中
    // indexOf() 回傳 -1 表示找不到
    if (numberchar.indexOf(ss.charAt(0)) >= 0)
        return true;  // 是 0~9 數字
    else
        return false; // 非數字
}

// ---------------------------------------------------------------
// 函式名稱：sayText()
// 功能：播放語音表中第 num 筆的語音內容
// 參數：num - 語音表索引（0~37）
// 傳回值：無
// 
// 說明：
//   此函式會從 voice_table 中取出對應的語音編號並播放
//   播放前會等待前一段語音結束
// ---------------------------------------------------------------
void sayText(uint8_t num)
{
    // 步驟1：等待前一段語音播放完畢
    if (isPlaying())
        while (isPlaying());

    // 步驟2：在模組閒置時開始播放指定語音
    while (!isPlaying())
    {
        // 播放 voice_table[num] 對應的 VOC_xx
        play(voice_table[num]);
    }
}

// ---------------------------------------------------------------
// 函式名稱：SpeakStringNumber()
// 功能：播放「數字字串」，將字串中的每個字元逐位念出
// 參數：snum - 要念讀的數字字串
//         例如："23.5" → 依序念出：2、3、「點」、5
//         "-10"   → 念出：「負」、1、0
// 傳回值：無
// 
// 支援的符號：
//   - 數字：0~9
//   - 小數點：「.」→ 念出「點」
//   - 負號：「-」→ 念出「負」
// ---------------------------------------------------------------
void SpeakStringNumber(String snum)
{
    if (snum.length() == 0)
        return; // 空字串不處理

    int le = snum.length();
    String cc;

    for (int i = 0; i < le; i++)
    {
        cc = snum.substring(i, i + 1); // 依序取出每一個字元

        if (checknum(cc))
        {
            // 若是 0~9 數字，轉成 int 之後呼叫 saynumber()
            saynumber(cc.toInt());
        }
        else
        {
            // 若是非數字，處理小數點與負號
            if (cc == ".")
                sayText(VOC_11); // VOC_11 對應「點」
            if (cc == "-")
                sayText(VOC_12); // VOC_12 對應「負」
        }
    }
}

