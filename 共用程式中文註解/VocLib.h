// ---------------------------------------------------------------
// 函式名稱：SayString() - 多載版本
// 功能：連續播放二段語音（例如「前門」+「入侵警示」）
// 參數：
//   vv - 第一段語音代碼（或索引）
//   vv2 - 第二段語音代碼（或索引）
// 傳回值：無
// 
// 播放流程：
//   1. 亮 LED
//   2. 播放第一段語音
//   3. 延遲 300ms 避免兩段語音連在一起
//   4. 播放第二段語音
//   5. 熄滅 LED
// ---------------------------------------------------------------
