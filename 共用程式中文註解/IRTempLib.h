/*
 * 檔案名稱：IRTempLib.h
 * 程式語言：Arduino C/C++
 * 檔案類型：標頭檔（Header File）- 紅外線溫度感測器驅動程式庫
 * 
 * ========================================
 * 程式功能完整解說
 * ========================================
 * 
 * 【檔案概述】
 *   本檔案為 BMH06203 紅外線（IR）溫度感測模組的驅動程式庫，
 *   提供非接觸式溫度測量功能。透過 I2C 通訊介面，可快速、
 *   準確地測量物體表面溫度，適用於人體體溫測量、工業設備
 *   溫度監測、物體表面溫度檢測等應用場景。
 * 
 * 【硬體規格】
 *   - 感測器模組：BMH06203 紅外線溫度感測模組
 *   - 感測原理：紅外線熱輻射測量（Stefan-Boltzmann 定律）
 *   - 通訊介面：I2C（Inter-Integrated Circuit）
 *   - 量測對象：物體表面溫度（Object Temperature）
 *   - 工作電壓：3.3V 或 5V（依模組規格）
 *   - 測量距離：通常為 5-15 公分（視鏡頭規格而定）
 * 
 * 【紅外線溫度測量原理說明】
 * 
 *   紅外線溫度測量是一種非接觸式溫度測量技術，其原理基於：
 * 
 *   1. 黑體輻射定律（Blackbody Radiation Law）：
 *      所有物體都會根據其溫度向外輻射紅外線能量
 *      - 物體溫度越高，輻射的紅外線能量越強
 *      - 輻射強度與溫度的四次方成正比（Stefan-Boltzmann 定律）
 * 
 *   2. 測量過程：
 *      a. 感測器接收目標物體輻射的紅外線能量
 *      b. 透過熱電堆（Thermopile）將紅外線能量轉換為電壓信號
 *      c. 內建放大器將微弱的電壓信號放大
 *      d. 類比數位轉換器（ADC）將電壓轉換為數位信號
 *      e. 微處理器根據校準曲線計算出溫度值
 * 
 *   3. 測量特點：
 *      - 非接觸式：無需接觸待測物體
 *      - 反應快速：通常在 0.5-1 秒內完成測量
 *      - 安全衛生：特別適合人體體溫測量
 *      - 可測高溫：可測量數百度的物體表面溫度
 * 
 * 【I2C 通訊說明】
 * 
 *   I2C（Inter-Integrated Circuit）是一種雙線式串列通訊協定：
 *   
 *   - SDA（Serial Data Line）：數據線，用於傳輸數據
 *   - SCL（Serial Clock Line）：時脈線，用於同步通訊時序
 *   - 主從架構：Arduino 為主控端（Master），感測器為從端（Slave）
 *   - 每個 I2C 裝置都有獨立的位址（Address）
 *   - 支援多組 I2C 匯流排（Wire、Wire1、Wire2 等）
 * 
 * 【主要功能】
 * 
 *   一、感測器初始化（initIRTemperature）
 *       - 啟動 I2C 通訊
 *       - 檢查感測器是否存在
 *       - 設定 I2C 位址
 *       - 輸出初始化完成訊息
 * 
 *   二、溫度讀取（readIRTemperature）
 *       - 讀取目標物體表面溫度
 *       - 透過 I2C 發送讀取指令
 *       - 接收並解析溫度數據
 *       - 回傳溫度值（單位：攝氏度）
 * 
 * 【運作流程】
 * 
 *   初始化階段（initIRTemperature）：
 *       1. 呼叫 mytherm.begin() 初始化感測器
 *          - 設定 I2C 通訊參數
 *          - 檢查感測器是否連接
 *          - 讀取感測器 ID 進行確認
 *       2. 輸出初始化成功訊息至序列監控視窗
 * 
 *   溫度讀取階段（readIRTemperature）：
 *       1. 呼叫 mytherm.readTemperature(OBJ_TEMP)
 *       2. 透過 I2C 發送溫度讀取指令
 *       3. 感測器進行溫度測量（約 0.5-1 秒）
 *       4. 感測器回傳溫度數據（通常為整數或浮點數）
 *       5. 函式庫自動轉換為攝氏溫度值
 *       6. 傳回溫度值
 * 
 * 【全域變數說明】
 *   - bodytemp：儲存體溫測量值的全域變數（float 型態）
 *   - mytherm：BMH06203 類別物件，用於與感測器通訊
 * 
 * 【I2C 匯流排選擇說明】
 * 
 *   Arduino / BMduino 可能提供多組 I2C 匯流排：
 *   
 *   - &Wire（主 I2C）：
 *       腳位：SDA（A4 / D18），SCL（A5 / D19）
 *       適用：Arduino Uno、Nano、Mega 等標準開發板
 *       注意：可能與其他 I2C 裝置共用
 *   
 *   - &Wire1（副 I2C 1）：
 *       腳位：SDA1（D15），SCL1（D14）
 *       適用：BMduino-UNO 等多 I2C 開發板
 *       優點：獨立匯流排，避免與其他裝置衝突
 *       本程式採用此選項
 *   
 *   - &Wire2（副 I2C 2）：
 *       腳位：依開發板規格而定
 *       適用：支援三組以上 I2C 的高階開發板
 * 
 * 【硬體接線說明】
 *   以 BMduino-UNO 為例（使用 Wire1）：
 *   
 *   BMH06203 模組    →    BMduino-UNO
 *   -----------------------------------------
 *   VCC              →    5V 或 3.3V
 *   GND              →    GND
 *   SDA（數據線）    →    SDA1（D15）
 *   SCL（時脈線）    →    SCL1（D14）
 *   
 *   注意事項：
 *   - 使用 Wire1 時，請確認開發板的 SDA1/SCL1 腳位
 *   - 建議使用 10kΩ 上拉電阻（通常模組已內建）
 *   - 線長不宜過長（建議 < 30 公分），避免信號衰減
 * 
 * 【注意事項】
 * 
 *   1. 測量距離：
 *      - 建議測量距離為 5-15 公分
 *      - 距離過遠可能導致測量誤差增大
 *      - 距離過近可能超出感測器視角範圍
 * 
 *   2. 測量角度：
 *      - 感測器應垂直對準待測物體表面
 *      - 傾斜角度過大（> 30°）可能影響準確度
 * 
 *   3. 環境影響：
 *      - 避免在強烈陽光或紅外線干擾環境下使用
 *      - 環境溫度變化會影響測量精度
 *      - 建議測量前讓感測器在環境中穩定 5-10 分鐘
 * 
 *   4. 人體體溫測量注意事項：
 *      - 建議測量額頭或手腕內側
 *      - 測量時保持 5-10 公分距離
 *      - 避免在運動後或流汗時立即測量
 *      - 建議連續測量 2-3 次取平均值
 * 
 *   5. 校準與精度：
 *      - 出廠時已進行校準，一般無需再次校準
 *      - 若需高精度測量，建議使用黑體爐進行校準
 *      - 精度通常為 ±0.5°C 或 ±2% 讀數
 * 
 * 【使用範例】
 * 
 *   #include "IRTempLib.h"
 *   
 *   void setup() {
 *       Serial.begin(9600);
 *       initIRTemperature();     // 初始化紅外線溫度感測器
 *       delay(1000);             // 等待感測器穩定
 *   }
 *   
 *   void loop() {
 *       // 讀取溫度值
 *       bodytemp = readIRTemperature();
 *       
 *       // 輸出至序列監控視窗
 *       Serial.print("物體溫度: ");
 *       Serial.print(bodytemp);
 *       Serial.println(" °C");
 *       
 *       // 判斷是否發燒（體溫測量應用）
 *       if (bodytemp > 37.5) {
 *           Serial.println("⚠️ 體溫偏高，請注意！");
 *       } else if (bodytemp > 36.0) {
 *           Serial.println("✅ 體溫正常");
 *       } else {
 *           Serial.println("⚠️ 體溫偏低，請確認測量方式");
 *       }
 *       
 *       delay(2000);  // 等待 2 秒再讀取
 *   }
 * 
 * 【版本資訊】
 *   最後修改日期：2026年3月26日
 *   適用專案：非接觸式溫度測量相關應用
 */

