/*-------------------------------------------------------------
   自訂函式：初始化紅外線溫度感測模組
   功能：啟動感測器並準備進行溫度測量
   
   執行流程：
     1. 呼叫 mytherm.begin() 初始化感測器
        - 設定 I2C 通訊參數（時脈速率、通訊協定）
        - 掃描 I2C 匯流排確認感測器存在
        - 讀取感測器 ID 進行驗證
        - 設定感測器工作模式（連續測量或單次測量）
     2. 輸出初始化成功訊息至序列監控視窗
   
   注意事項：
     - 必須在首次讀取溫度前呼叫此函式
     - 若感測器未連接或 I2C 位址錯誤，程式可能卡住
     - 建議在初始化後等待 1-2 秒，讓感測器穩定
--------------------------------------------------------------*/
void initIRTemperature()    // 初始化紅外線模組
{
  /* ===== 系統初始化階段 ===== */
  
  // 啟動感測器的初始化程序
  // begin() 函式會執行以下操作：
  //   1. 設定 I2C 通訊速率（通常為 100kHz 或 400kHz）
  //   2. 檢查感測器是否存在於指定的 I2C 位址
  //   3. 讀取感測器版本號或 ID 進行確認
  //   4. 設定感測器進入可通訊狀態
  //   5. 配置感測器內部暫存器（如取樣率、平均次數等）
  mytherm.begin();
  
  // 輸出初始化完成訊息至序列監控視窗
  // 用於偵錯和確認感測器初始化是否成功
  // 注意：訊息中未包含換行符號，可根據需要修改為 println()
  Serial.println("初始化紅外線模組");
}

/*-------------------------------------------------------------
   自訂函式：讀取紅外線溫度感測器的量測值
   功能：獲取當前物體表面溫度
   
   執行流程：
     1. 呼叫 mytherm.readTemperature(OBJ_TEMP) 函式
        - OBJ_TEMP：指定讀取物體溫度（Object Temperature）
        - 感測器支援多種測量模式（物體溫度、環境溫度等）
     2. 函式內部執行：
        a. 透過 I2C 發送溫度讀取命令
        b. 等待感測器完成測量（約 0.5-1 秒）
        c. 讀取感測器內部暫存器中的溫度數據
        d. 將原始數據轉換為攝氏溫度值
        e. 進行必要的補償與校正
     3. 回傳溫度值（浮點數）
   
   傳回值說明：
     - 正常情況：回傳物體表面溫度（攝氏度）
     - 測量失敗：可能回傳 0 或上次成功讀取的值
     - 超出量程：回傳最大/最小可測量值
   
   注意事項：
     - 呼叫前需確保感測器已初始化
     - 建議讀取間隔不低於 1 秒，給感測器足夠的測量時間
     - 對於移動中的物體，可能需要更快的讀取速度
--------------------------------------------------------------*/
float readIRTemperature() // 讀取紅外線模組取得量測溫度
{
    // 讀取紅外線模組取得的物體溫度並直接傳回
    // mytherm.readTemperature(OBJ_TEMP) 參數說明：
    //   OBJ_TEMP：物體溫度（Object Temperature）常數
    //   AMB_TEMP：環境溫度（Ambient Temperature）常數（若支援）
    // 
    // 回傳值：float 型態的溫度值（攝氏度）
    return mytherm.readTemperature(OBJ_TEMP);
}