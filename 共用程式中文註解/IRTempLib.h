/*
 * 變數名稱：bodytemp
 * 資料型態：float（單精度浮點數）
 * 功能說明：儲存紅外線感測器讀取的溫度值
 * 單位：攝氏度（°C）
 * 用途：供其他模組直接讀取，無需每次呼叫 readIRTemperature()
 * 備註：可根據應用場景命名為其他名稱（如 objectTemp、surfaceTemp 等）
 */
float bodytemp = 0;  // 體溫全域變數

