/*-------------------------------------------------------------
  程式名稱：人體移動偵測模組 (PIR / mmWave Sensor Control Module)
  程式用途：本程式用於控制 BM22S4221-1 人感模組（PIR / mmWave），
           可偵測環境中的人員或物體移動，並透過狀態腳位與 LED 指示燈
           顯示目前是否處於警報狀態。
  
  硬體架構：支援 BMduino 或一般 Arduino 開發板，可選擇使用軟體序列埠
           或硬體序列埠（Serial1~Serial4）與感測器通訊。
  
  作者說明：本程式為 Arduino C 語言撰寫，適用於人員移動偵測、入侵警報、
           自動化控制等應用場景。
  
  運作原理：
    1. 感測器持續偵測環境中是否有移動的物體或人員。
    2. 狀態腳位（STATUS）輸出 HIGH 或 LOW 代表不同狀態。
    3. 程式讀取感測器狀態並透過內建 LED（腳位 13）顯示：
       - 正常狀態（未偵測到）：LED 每秒閃爍一次（亮 100ms）
       - 警報狀態（偵測到物體）：LED 持續亮起（每次亮 1 秒）
    4. 當狀態發生改變時，會透過序列埠監控視窗輸出對應訊息。
  
  狀態說明：
    a. 模組正常（flag = 0）：沒有偵測到移動物體、沒有入侵警示。
       → 腳位 13 的 LED 每秒閃爍一次（亮 100ms）。
    b. 模組警報（flag = 1）：偵測到物體或人員經過。
       → 腳位 13 的 LED 會持續亮起（每次亮 1 秒）。
  
  最後修改：2026年
-------------------------------------------------------------*/

//--PIR偵測模組(BMCOM) BMA46M422 自訂函式區

//----------外部引用函式區----------------
/*****************************************************************
File:         readStatusPin     
Description:  
  本程式示範如何讀取 BM22S4221-1 人感模組（PIR / mmWave）的狀態腳位，
  並依照不同狀態讓 LED 產生不同的燈號效果，藉此顯示裝置目前是否偵測到物體。

  狀態說明：
    a. 模組正常（flag = 0）：沒有偵測到移動物體、沒有入侵警示。
       → 腳位 13 的 LED 每秒閃爍一次（亮 100ms）。

    b. 模組警報（flag = 1）：偵測到物體或人員經過。
       → 腳位 13 的 LED 會持續亮起（每次亮 1 秒）。

  另外：
    當模組狀態改變（由正常 → 警報 或 警報 → 正常），
    系統會透過序列埠（Serial Monitor）印出對應訊息。
******************************************************************/

#include "BM22S4221-1.h"       // 引入 BM22S4221-1 感測模組專用函式庫

//----------感測元件物件區---------------
// ---- 感測器物件建立方式（只能擇一使用）--------------------------------------
// BM22S4221_1 PIR(STATUS,6,7);   // 使用軟體序列：intPin=5, rxPin=6, txPin=7
BM22S4221_1 PIR(22, &Serial1);  // 使用硬體序列 Serial1，intPin = 22（BMduino 預設支援）
// BM22S4221_1 PIR(29, &Serial2); // 使用硬體序列 Serial2
// BM22S4221_1 PIR(STATUS, &Serial3); // 使用硬體序列 Serial3
// BM22S4221_1 PIR(STATUS, &Serial4); // 使用硬體序列 Serial4
// ------------------------------------------------------------------------------

//------------全域變數區----------------

uint8_t flag;                 // 旗標變數，用來記錄目前感測器的工作狀態
                             // flag = 0 → 正常模式（未偵測到物體）
                             // flag = 1 → 警報模式（偵測到物體）
                             
uint8_t STATUS = 5;           // 設定模組的「狀態腳位」，此腳位會輸出 HIGH / LOW 代表狀態
                             // 注意：此腳位用於連接感測器的狀態輸出接腳
                             // 實際應用中需根據硬體接線調整此值

/*-------------------------------------------------------------
   自訂函式宣告
--------------------------------------------------------------*/
void initPIR();              // 初始化 PIR 偵測模組（設定通訊、腳位模式）
boolean readPIR();           // 讀取 PIR 偵測模組狀態（回傳是否偵測到物體）

//-----------自訂函式實體區--------------

//-------------------------------------------------------------
// 函式名稱：initPIR
// 功能說明：初始化人體移動偵測模組，啟動通訊並設定相關腳位模式
// 輸入參數：無
// 回傳值：無
//-------------------------------------------------------------
void initPIR()  //初始化PIR偵測模組(BMCOM) BMA46M422
{
  PIR.begin();                 // 初始化 BM22S4221-1 模組
                               // 此函式會啟動串列通訊（軟體或硬體序列埠）
                               // 並對模組進行基本設定，使其進入工作狀態

  pinMode(STATUS, INPUT);      // 將感測器狀態腳位設為「輸入模式」
                               // 此腳位用於接收感測器輸出的狀態訊號
                               // 當偵測到物體時，此腳位會輸出 HIGH（高電位）
                               // 未偵測到時輸出 LOW（低電位）
  
  pinMode(13, OUTPUT);         // 將內建 LED 腳位（13）設定為輸出模式
                               // 此 LED 用於視覺化顯示感測器狀態
                               // 方便開發者或使用者即時了解目前是否偵測到物體
}

//-------------------------------------------------------------
// 函式名稱：readPIR
// 功能說明：讀取 PIR 偵測模組的當前狀態
// 輸入參數：無
// 回傳值：布林值 (boolean)
//         - true（1）：偵測到物體或人員移動（警報狀態）
//         - false（0）：未偵測到任何移動（正常狀態）
//-------------------------------------------------------------
boolean readPIR() //讀取PIR偵測模組(BMCOM)資料
{
    return PIR.getSTATUS(); // 呼叫函式庫提供的 getSTATUS() 方法
                            // 此方法會透過狀態腳位讀取感測器目前的狀態
                            // 並將結果以布林值形式回傳
                            // true = 偵測到物體，false = 未偵測到
}