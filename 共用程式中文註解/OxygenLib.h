/*******************************************************
 * 程式名稱：血氧/心率感測器控制模組 (Oxygen & Heart Rate Sensor Control Module)
 * 程式用途：本程式用於控制 BMH08002-4 血氧/心率感測器，支援查詢模式與連續模式，
 *           可讀取血氧濃度(SpO2)、心率(BPM)及灌注指數(PI)等生理參數。
 * 硬體架構：支援 BMduino 或一般 Arduino 開發板，可選擇使用軟體序列埠或硬體序列埠連接。
 * 作者說明：本程式為 Arduino C 語言撰寫，適用於生理訊號量測應用。
 * 使用方式：
 *   1. 將手指放置於感測器上，保持穩定以獲得準確讀值。
 *   2. 可透過修改 Mode 變數或 setModeConfig 切換查詢模式與連續模式。
 *   3. 查詢模式：每次呼叫 Mode_ask() 會主動請求一次量測資料。
 *   4. 連續模式：感測器會持續傳送資料，需定期呼叫 Mode_continuous_timing() 讀取。
 * 最後修改：2026年
 *******************************************************/

//----------外部引用函式區----------------
#include <BMH08002-4.h>  // 引入血氧感測器驅動程式庫

/********************* 感測器物件宣告 ************************/
// ❶ 預設以「軟體序列埠」方式建立物件（請依實機腳位修改）
BMH08002_4 mySpo2(8, 7, 6);      // enPin=8（使能腳）, rxPin=7（接收腳，接感測器TX）, txPin=6（傳送腳，接感測器RX）

// ❷ 若要改用「硬體序列埠」，請改用下列其中一行（解除註解並註解掉上面那行）
// BMH08002_4 mySpo2(22, &Serial1);  // enPin=22，綁定 Serial1 硬體序列埠
// BMH08002_4 mySpo2(25, &Serial2);  // enPin=25，綁定 Serial2 硬體序列埠
// BMH08002_4 mySpo2(2,  &Serial3);  // enPin=2， 綁定 Serial3 硬體序列埠
// BMH08002_4 mySpo2(2,  &Serial4);  // enPin=2， 綁定 Serial4 硬體序列埠

/********************* 全域變數 ************************/
uint8_t Mode = 0;           // 讀取模式：0=連續模式(Streaming)，1=查詢/回應模式(Query)
uint8_t rBuf[15] = {0};     // 感測器資料封包緩衝區（固定長度 15 bytes，儲存原始資料）
uint8_t Status = 0;         // 狀態碼（由感測器回報，表示目前量測狀態）
uint8_t flag = 0;           // 提示旗標（避免重複顯示相同的提醒訊息，減少序列埠輸出負擔）

// 讀值快取（便於其他模組或主程式直接使用）
int oxyvalue = 0;           // SpO2 血氧飽和度，單位為百分比 (%)
int hbvalue  = 0;           // 心率，單位為每分鐘心跳數 (BPM)
int pivalue  = 0;           // 灌注指數 PI（原始值需除以 10 得到實際百分比，例如 35 表示 3.5%）
int readok   = 0;           // 資料是否成功更新（1=成功取得有效讀值；0=未就緒或讀取失敗）

/********************* 前置宣告 ************************/
void initOxygen();                 // 初始化感測器（含使能腳設定、模式配置、時間間隔設定）
void readOxygen();                 // 開始量測並判定目前運作模式（查詢模式或連續模式）
void Mode_ask();                   // 查詢模式：主動請求一次資料封包，等待量測完成後解析資料
void Mode_continuous_timing();     // 連續模式：輪詢是否有新資料可讀，若有則讀取並解析

/********************* 初始化：設定量測參數 ************************/
// 函式名稱：initOxygen
// 功能說明：初始化血氧感測器，設定運作模式與量測間隔，並輸出操作提示
// 輸入參數：無
// 回傳值：無
void initOxygen()
{
  mySpo2.begin();                // 初始化感測器（包含使能 enPin 腳位設定，啟動感測器電源）
  mySpo2.setModeConfig(0x02);    // 設定為「查詢回應模式」(0x02)；0x00/0x01 通常為連續模式
  mySpo2.setTimeInterval(300);   // 設定量測間隔為 300 毫秒（在查詢模式下，此為回應時間間隔）
  Serial.println("請放置您的手指，保持穩定以獲得較佳量測結果。");
}

/********************* 進入量測並判定模式 ************************/
// 函式名稱：readOxygen
// 功能說明：開始進行量測，並根據感測器目前設定的模式，判斷應使用查詢模式或連續模式
// 輸入參數：無
// 回傳值：無
void readOxygen()
{
  mySpo2.beginMeasure();           // 送出開始量測指令（感測器紅燈亮起，表示正在進行量測）

  // 讀回目前感測器設定的運作模式
  Mode = mySpo2.getModeConfig();

  // 0x02 / 0x03：屬於「查詢回應模式」族群 → 標記為查詢模式
  if (Mode == 0x02 || Mode == 0x03) {
    Mode = 1;  // 設定為查詢模式（Query Mode）
  } else {
    Mode = 0;  // 其他模式（0x00, 0x01）視為連續模式（Continuous Mode），裝置會主動持續傳送資料
  }
}

/********************* 查詢一次資料封包 ************************/
// 函式名稱：Mode_ask
// 功能說明：查詢模式專用函式，主動向感測器請求一次量測資料封包，
//           根據回傳的狀態碼判斷量測進度，並在量測成功時解析資料
// 輸入參數：無
// 回傳值：無
// 狀態碼說明：
//   0x00：未偵測到手指
//   0x01：已偵測到手指但量測中，可能因移動導致不穩定
//   0x02：量測完成，資料有效
void Mode_ask()
{
  // 主動請求資料，成功時 rBuf 會被填入 15 bytes 的完整封包；回傳的 Status 為狀態碼
  Status = mySpo2.requestInfoPackage(rBuf);
  readok = 0;  // 先預設此次讀取尚未成功，待確認成功後再設為 1

  // 0x02：一次有效量測完成，資料可供讀取
  if (Status == 0x02) {
    Serial.println("測量完成，可以移開手指。");

    // rBuf[0]：SpO2 血氧飽和度（百分比）
    Serial.print("SpO2: ");
    Serial.print(rBuf[0], DEC);
    Serial.println("%");
    oxyvalue = rBuf[0];  // 儲存到全域變數供其他程式使用

    // rBuf[1]：心率（每分鐘心跳數，BPM）
    Serial.print("心率: ");
    Serial.print(rBuf[1], DEC);
    Serial.println(" BPM");
    hbvalue = rBuf[1];   // 儲存到全域變數

    // rBuf[2]：灌注指數 PI（原始值需除以 10 得到實際百分比）
    Serial.print("PI: ");
    Serial.print((float)rBuf[2] / 10.0f);
    Serial.println("%");
    pivalue = rBuf[2];   // 儲存原始值，使用時需轉換

    readok = 1;            // 標記本輪讀取成功
   // mySpo2.endMeasure();   // 可選擇結束量測（避免持續耗電）
   // mySpo2.sleep();        // 進入低功耗模式（如需省電可啟用）
  }

  // 0x01：偵測到手指但量測中，且疑似有移動干擾 → 提示使用者保持穩定
  // 使用 flag 變數避免重複輸出相同訊息
  if (Status == 0x01 && flag != 1) {
    Serial.println("請勿移動您的手指（保持靜止以利量測）。");
    flag = 1;      // 設定旗標，避免重複顯示相同提醒
    readok = 0;
  }

  // 0x00：未檢測到手指 → 請使用者重新放置手指
  // 當 flag 不為 0 時才顯示，避免過多重複訊息
  if (Status == 0x00 && flag != 0) {
    Serial.println("未偵測到手指，請重新放置並覆蓋感測區。");
    flag = 0;      // 重置旗標，待下次未偵測時可再次提示
    readok = 0;
  }
}

/********************* 連續模式取樣：有資料就讀出 ************************/
// 函式名稱：Mode_continuous_timing
// 功能說明：連續模式專用函式，輪詢檢查感測器是否有新資料可讀，
//           若有則讀取一筆 15 bytes 封包，並解析關鍵資料欄位
// 輸入參數：無
// 回傳值：無
void Mode_continuous_timing()
{
  // 連續模式下，感測器會主動將資料推送到 UART 序列埠
  // 此處輪詢檢查接收緩衝區是否有完整封包可讀
  if (mySpo2.isInfoAvailable()) {
    mySpo2.readInfoPackage(rBuf); // 將一筆 15 bytes 的封包讀入 rBuf 緩衝區

    // 以下為除錯用：逐位輸出封包內容（可協助驗證封包結構）
    for (uint8_t i = 0; i < 15; i++) {
      Serial.print(i);
      Serial.print(": ");
      Serial.println(rBuf[i]);
    }

    // 解析關鍵欄位，與查詢模式相同
    oxyvalue = rBuf[0];   // 血氧濃度（%）
    hbvalue  = rBuf[1];   // 心率（BPM）
    pivalue  = rBuf[2];   // 灌注指數原始值（實際百分比需除以 10）
    readok   = 1;         // 標記讀取成功
  }
}