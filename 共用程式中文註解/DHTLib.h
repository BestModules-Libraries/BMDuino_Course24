//   3. 傳回序號字串
// 
// 用途說明：
//   - 每個感測器的唯一識別碼
//   - 用於設備追蹤與管理
//   - 可作為雲端資料上傳的設備 ID
// 
// 注意事項：
//   函式名稱拼寫錯誤："getSNNumbetr" 應為 "getSNNumber"
//   使用時請注意正確的函式名稱，避免編譯錯誤
// ---------------------------------------------------------------
String getSNNumbetr()   //讀取產品序號（Serial Number）
{
    // 讀取產品序號（SN）
    // BMht.getSN() 讀取感測器的唯一序列號
    // 序號通常為多個位元組的組合，此處轉為字串表示
    return String(BMht.getSN());
}

// ---------------------------------------------------------------
// 函式名稱：ShowDHTInformation()
// 功能：印出溫溼度感測器的完整產品資訊
// 參數：無
// 傳回值：無
// 
// 輸出格式範例：
//   =======DeviceInfo(HEX)=========
//   SN : 12345678
//   PID : 25S2021
//   VER : 1.0
//   ===============================
// 
// 執行流程：
//   1. 輸出標題分隔線
//   2. 呼叫 getSNNumbetr() 取得序號並輸出
//   3. 呼叫 getProductNumbetr() 取得產品編號並輸出
//   4. 呼叫 getVersion() 取得韌體版本並輸出
//   5. 輸出結束分隔線
// 
// 用途說明：
//   - 快速確認感測器資訊
//   - 便於偵錯與硬體確認
//   - 可在序列監控視窗中直接查看
// ---------------------------------------------------------------
void ShowDHTInformation()   //印出溫溼度感測器的完整產品資訊
{
    // 輸出資訊標題與分隔線
    // 註明以十六進位格式顯示（實際輸出可能為十進位）
    Serial.println("=======DeviceInfo(HEX)========= ");
    
    // 輸出產品序號（SN）
    Serial.print("SN : ");
    Serial.println(getSNNumbetr());  // 以十六進位格式顯示序號
    
    // 輸出產品編號（PID）
    Serial.print("PID : ");
    Serial.println(getProductNumbetr());  // 顯示產品 ID
    
    // 輸出韌體版本（VER）
    Serial.print("VER : ");
    Serial.println(getVersion());  // 顯示韌體版本
    
    // 輸出結束分隔線
    Serial.println("=============================== ");
}