/*
 * 檔案名稱：DHTLib.h
 * 程式語言：Arduino C/C++
 * 檔案類型：標頭檔（Header File）- 溫溼度感測器驅動程式庫
 * 
 * ========================================
 * 程式功能完整解說
 * ========================================
 * 
 * 【檔案概述】
 *   本檔案為 BM25S2021-1 溫溼度感測模組的驅動程式庫，提供完整的
 *   感測器初始化、資料讀取、裝置資訊查詢等功能。透過 I2C 通訊介面，
 *   可輕鬆讀取環境溫度與濕度數據，適用於智慧農業、環境監測、
 *   居家自動化等應用場景。
 * 
 * 【硬體規格】
 *   - 感測器模組：BM25S2021-1 溫溼度感測模組
 *   - 通訊介面：I2C（本程式使用 Wire1 介面）
 *   - 溫度測量範圍：-40°C ～ +85°C
 *   - 濕度測量範圍：0% ～ 100% RH
 *   - 解析度：溫度 0.01°C，濕度 0.01% RH
 * 
 * 【主要功能】
 *   1. 感測器初始化（initDHT）
 *      - 啟動 I2C 通訊
 *      - 與感測器建立連線
 *      - 輸出初始化成功訊息
 * 
 *   2. 環境數據讀取
 *      - readTemperature()：讀取當前溫度值（攝氏度）
 *      - readHumidity()：讀取當前濕度值（百分比）
 * 
 *   3. 裝置資訊查詢
 *      - getVersion()：讀取韌體版本號
 *      - getProductNumber()：讀取產品編號（Product ID）
 *      - getSNNumber()：讀取產品序號（Serial Number）
 *      - ShowDHTInformation()：顯示完整裝置資訊
 * 
 * 【I2C 通訊說明】
 *   I2C（Inter-Integrated Circuit）是一種雙線式串列通訊協定：
 *   - SDA（數據線）：用於傳輸數據
 *   - SCL（時脈線）：用於同步通訊時序
 *   
 *   本程式使用 Wire1 介面，適用於支援多組 I2C 的開發板
 *   （如 BMduino、Arduino Due、ESP32 等）
 * 
 * 【運作流程】
 * 
 *   初始化階段：
 *       1. 呼叫 initDHT() 函式
 *       2. BMht.begin() 啟動 I2C 通訊
 *       3. 感測器進入就緒狀態
 *       4. 序列埠輸出初始化成功訊息
 * 
 *   數據讀取階段：
 *       1. 呼叫 readTemperature() 或 readHumidity()
 *       2. BMht.readTemperature() / BMht.readHumidity() 發送 I2C 讀取指令
 *       3. 感測器回傳原始數據
 *       4. 函式庫自動轉換為實際物理量（°C 或 %）
 *       5. 傳回浮點數值
 * 
 *   資訊查詢階段：
 *       1. 呼叫對應的查詢函式（getVersion / getProductNumber / getSNNumber）
 *       2. BMht.getFWVer() / getPID() / getSN() 讀取裝置內部暫存器
 *       3. 將數值轉換為字串格式
 *       4. 傳回結果
 * 
 * 【全域變數說明】
 *   - TValue：儲存最新讀取的溫度值（單位：攝氏度）
 *   - HValue：儲存最新讀取的濕度值（單位：百分比）
 *   - DHTStr：儲存感測器資訊字串（序號、產品ID、韌體版本）
 * 
 * 【相依函式庫】
 *   - String.h：Arduino 內建字串處理函式庫
 *   - BM25S2021-1.h：BM25S2021-1 感測器底層驅動函式庫
 * 
 * 【注意事項】
 *   1. I2C 腳位連接：
 *      - Wire1 介面的 SDA 與 SCL 腳位依開發板而定
 *      - BMduino UNO：SDA1 (D15)，SCL1 (D14)
 *      - 使用前請確認開發板的 I2C 腳位定義
 * 
 *   2. 電源供應：
 *      - 感測器工作電壓：3.3V 或 5V（依模組規格）
 *      - 確保電源穩定以獲得準確讀數
 * 
 *   3. 讀取頻率：
 *      - 建議讀取間隔不低於 2 秒
 *      - 過於頻繁讀取可能影響感測器精度
 * 
 *   4. 程式碼注意事項：
 *      - getProductNumbetr() 與 getSNNumbetr() 函式名稱有拼寫錯誤
 *      - 實際使用時請注意正確的函式名稱
 * 
 * 【使用範例】
 * 
 *   void setup() {
 *       Serial.begin(9600);
 *       initDHT();                    // 初始化溫溼度感測器
 *       ShowDHTInformation();         // 顯示感測器資訊
 *   }
 *   
 *   void loop() {
 *       float temp = readTemperature(); // 讀取溫度
 *       float humi = readHumidity();    // 讀取濕度
 *       
 *       Serial.print("溫度: ");
 *       Serial.print(temp);
 *       Serial.println(" °C");
 *       
 *       Serial.print("濕度: ");
 *       Serial.print(humi);
 *       Serial.println(" %");
 *       
 *       delay(2000);  // 等待 2 秒再讀取
 *   }
 * 
 * 【版本資訊】
 *   最後修改日期：2026年3月26日
 *   適用專案：溫溼度監測相關應用
 */

