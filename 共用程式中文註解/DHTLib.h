#include "String.h"

// 引入 BM25S2021-1 溫溼度感測模組專用函式庫
// 該函式庫提供與 BM25S2021-1 感測器通訊的底層函式
// 包含 I2C 通訊協定的實作與數據轉換功能
#include "BM25S2021-1.h"

