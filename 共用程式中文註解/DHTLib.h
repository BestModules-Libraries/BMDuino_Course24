// ---------------------------------------------------------------
// 函式名稱：initDHT()
// 功能：初始化溫溼度感測器，啟動 I2C 通訊
// 參數：無
// 傳回值：無
// 
// 執行流程：
//   1. 呼叫 BMht.begin() 初始化感測器
//      - 啟動 I2C 通訊協定
//      - 設定感測器內部暫存器
//      - 準備接收讀取指令
//   2. 序列埠輸出初始化成功訊息
//      - 用於確認感測器初始化是否成功
//      - 方便偵錯與狀態監控
// 
// 注意事項：
//   - 必須在 setup() 中呼叫此函式
//   - 若初始化失敗，請檢查 I2C 接線與電源供應
// ---------------------------------------------------------------
void initDHT()   // 初始化感測器，啟動 I2C 通訊
{
    // 初始化感測器，啟動 I2C 通訊
    // 此函式會設定 I2C 通訊參數並與感測器建立連線
    // 若感測器未連接或 I2C 位址錯誤，此函式可能導致程式卡住
    BMht.begin();
    
    // 在序列埠輸出初始化成功訊息
    // 用於偵錯和確認感測器初始化是否成功
    // 注意：訊息中的拼寫錯誤（"Temerature" 應為 "Temperature"）
    Serial.println("init Temperature OK");
}

// ---------------------------------------------------------------
// 函式名稱：readTemperature()
// 功能：讀取溫度值
// 參數：無
// 傳回值：float - 溫度值（攝氏度）
// 
// 執行流程：
//   1. 呼叫 BMht.readTemperature() 讀取感測器溫度資料
//      - 透過 I2C 發送讀取指令
//      - 感測器回傳原始數據（通常為整數）
//      - 函式庫自動轉換為實際溫度值（浮點數）
//   2. 直接傳回溫度值
// 
// 注意事項：
//   - 建議在呼叫前確認感測器已初始化
//   - 讀取失敗時可能傳回 0 或上次成功讀取的值
//   - 建議設定合理的讀取間隔（至少 2 秒）
// ---------------------------------------------------------------
float readTemperature() // 讀取溫度值並直接傳回
{
    // 讀取溫度值並直接傳回
    // BMht.readTemperature() 會透過 I2C 向感測器發送讀取指令
    // 並將讀取的原始資料轉換為攝氏溫度值
    // 傳回值範圍：-40.00 ～ +85.00
    return BMht.readTemperature();
}

// ---------------------------------------------------------------
// 函式名稱：readHumidity()
// 功能：讀取濕度值
// 參數：無
// 傳回值：float - 濕度值（百分比）
// 
// 執行流程：
//   1. 呼叫 BMht.readHumidity() 讀取感測器濕度資料
//      - 透過 I2C 發送讀取指令
//      - 感測器回傳原始數據
//      - 函式庫自動轉換為實際濕度值
//   2. 直接傳回濕度值
// 
// 注意事項：
//   - 建議在呼叫前確認感測器已初始化
//   - 濕度測量容易受溫度影響，建議同時記錄溫度值
// ---------------------------------------------------------------
float readHumidity()    //讀取濕度值
{
    // 讀取濕度值並直接傳回
    // BMht.readHumidity() 會透過 I2C 向感測器發送讀取指令
    // 並將讀取的原始資料轉換為百分比濕度值
    // 傳回值範圍：0.00 ～ 100.00
    return BMht.readHumidity();
}

// ---------------------------------------------------------------
// 函式名稱：getVersion()
// 功能：讀取感測器韌體版本號
// 參數：無
// 傳回值：String - 韌體版本字串
// 
// 執行流程：
//   1. 呼叫 BMht.getFWVer() 讀取韌體版本
//      - 讀取感測器內部暫存器中的版本資訊
//      - 版本號通常為整數或十六進位值
//   2. 透過 String() 轉換為字串格式
//   3. 傳回版本字串
// 
// 用途說明：
//   - 確認感測器韌體版本
//   - 檢查是否支援特定功能
//   - 用於除錯與相容性測試
// ---------------------------------------------------------------
String getVersion() // 讀取韌體版本號
{
    // 讀取韌體版本號
    // BMht.getFWVer() 傳回的是內建的資料型別（可能為 int 或 byte）
    // 透過 String() 轉換為字串，方便串接與輸出
    return String(BMht.getFWVer());
}

// ---------------------------------------------------------------
// 函式名稱：getProductNumber()
// 功能：讀取產品編號（Product ID / PID）
// 參數：無
// 傳回值：String - 產品編號字串
// 
// 執行流程：
//   1. 呼叫 BMht.getPID() 讀取產品識別碼
//   2. 將數值轉換為字串
//   3. 傳回產品編號
// 
// 用途說明：
//   - 確認感測器型號
//   - 確保使用正確的感測器模組
//   - 用於自動識別硬體配置
// 
// 注意事項：
//   函式名稱拼寫錯誤："getProductNumbetr" 應為 "getProductNumber"
//   使用時請注意正確的函式名稱，避免編譯錯誤
// ---------------------------------------------------------------
String getProductNumbetr()  //讀取產品編號（Product ID）
{
    // 讀取產品編號（PID）
    // BMht.getPID() 讀取感測器的產品識別碼
    // 不同型號的感測器會有不同的 PID 值
    return String(BMht.getPID());
}

// ---------------------------------------------------------------
// 函式名稱：getSNNumber()
// 功能：讀取產品序號（Serial Number / SN）
// 參數：無
// 傳回值：String - 產品序號字串
// 
// 執行流程：
//   1. 呼叫 BMht.getSN() 讀取產品序號
